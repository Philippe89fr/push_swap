#include "libft.h"
#include <stdlib.h>

t_list	*ft_lstnew(long long content)
{
	t_list	*new;

	new = (t_list *)malloc(sizeof(t_list));
	if (new == NULL)
		return (NULL);
	new->content = content;
	new->next = NULL;
	return (new);
}
